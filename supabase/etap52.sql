-- RX Team — Етап 52 (картка гравця: бекфіл дати патча для легасі-записів).
-- Виконати в Supabase → SQL Editor. Ідемпотентно (оновлює лише рядки з порожньою датою).
--
-- Публічна картка показує «Учасник команди з <дата>» (patch_at) і «Зареєстрований з <дата>»
-- (created_at). created_at є в усіх. patch_at міг лишитись NULL у тих, хто отримав патч ДО
-- того, як почали фіксувати дату — таким легасі-записам вписуємо сьогоднішню дату, щоб на
-- картці була дата, а не порожньо.

update players set patch_at = now() where has_patch = true and patch_at is null;
